    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        selectedIndex = jTable1.getSelectedRow();
        int recordID = (int) jTable1.getValueAt(selectedIndex, 0) - 1;
        int exerciseID = (int) jTable1.getValueAt(selectedIndex, 2) - 1;

        if (recordID >= 0 && recordID < _loginUI.loadedRecords.size() && exerciseID >= 0 && exerciseID < _loginUI.loadedExercises.size()) {
            currRecord = _loginUI.loadedRecords.get(recordID);
            currExercise = _loginUI.loadedExercises.get(exerciseID);

            for (LeaderBoard lb : currExercise.getLeaderBoard()) {
                if (lb.getRecordID() == currRecord.getId()) {
                    currLeaderBoard = lb;
                    break;
                }
            }
            displayInformation();       // tablodan kayıt seçildiğinde o kayıda ait her şey ekrana yazdırılıyor
            if (currLeaderBoard == null) {
                jLabel4.setText("Veriye Ulaşılamadı!!");
                jLabel5.setText("Veriye Ulaşılamadı!!");
                jLabel6.setText("Veriye Ulaşılamadı!!");
                jLabel8.setText("Veriye Ulaşılamadı!!");
                jButton2.setText("Veriye Ulaşılamadı!!");
            } else {
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd LLLL yyyy");
                jLabel4.setText(currRecord.getDate().format(formatter));
                jLabel5.setText(Double.toString(currLeaderBoard.getTruthPoint()));
                jLabel6.setText(Double.toString(currLeaderBoard.getTimePoint()));
                int minI = currRecord.getTotalTime() / 60;
                String minS = Integer.toString(minI);
                if (minI < 9) minS = "0" + minS;
                int secI = currRecord.getTotalTime() % 60;
                String secS = Integer.toString(secI);
                if (secI < 9) secS = "0" + secS;
                jLabel8.setText(minS + ":" + secS);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

