    private void displayRecords() {         // kayıtları gösteriyor
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        for (Record rec : _loginUI.loadedRecords) {
            Object[] rowData = new Object[3];
            rowData[0] = rec.getId();
            rowData[1] = rec.getChild().getFullName();
            rowData[2] = rec.getExercise().getId();
            model.addRow(rowData);
        }
        jTable1.setModel(model);
    }
    
    private void displayInformation() {     // kayıt bilgileri gösteriliyor
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        int[][] ab = currExercise.questions;
        ArrayList<Integer> answers = currRecord.getAnswers();
        ArrayList<Integer> times = currRecord.getTimes();
        ArrayList<String> truth = currRecord.getTruth();
        for(int i=0; i<currExercise.getN(); i++){
            Object[] rowData = new Object[6];
            rowData[0] = ab[i][0];
            rowData[1] = ab[i][1];
            rowData[2] = ab[i][2];
            rowData[3] = answers.get(i);
            rowData[4] = truth.get(i);
            rowData[5] = times.get(i);
            
            model.addRow(rowData);
        }
        jTable2.setModel(model);
    }
    
