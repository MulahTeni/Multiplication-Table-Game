    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            selectedIndex = jTable1.getSelectedRow();
            if (selectedIndex == -1) 
                throw new Exception("Kullanıcı Seçtiğinizden Emin Olun!");
            String userName = (String) jTable1.getValueAt(selectedIndex, 2);
            int i=0;
            for(Child c : _loginUI.loadedChildren){
                if(c.getUserName().equals(userName)){
                    _loginUI.loadedChildren.remove(i);
                    Functions.saveChildren(_loginUI.loadedChildren, "children.ser");
                    model.removeRow(selectedIndex);
                    break;
                }
                i++;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Hata Mesajı", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_jButton2ActionPerformed

