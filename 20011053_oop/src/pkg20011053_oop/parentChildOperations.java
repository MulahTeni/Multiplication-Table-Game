    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String fullName = jTextField1.getText();
        String password = jTextField2.getText();
        try {
            if (fullName.isEmpty() || password.isEmpty())   // çocuk ekliyor otomatik id atanıyor pop up olarak gösteriliyor
                throw new Exception("Kullanıcı adı ve şifre girmelisiniz!");
            Child newChild = new Child(fullName, password);
            _loginUI.loadedChildren.add(newChild);
            String mesaj = "Kullanıcı Adı: " + newChild.getUserName() + "\nŞifre: " + newChild.getPassword();
            JOptionPane.showMessageDialog(null, mesaj, "Kullanıcı Bilgileri", JOptionPane.INFORMATION_MESSAGE);
            Functions.saveChildren(_loginUI.loadedChildren, "children.ser");
            Object[] list = new Object[4];
            list[0] = newChild.getId();
            list[1] = newChild.getFullName();
            list[2] = newChild.getUserName();
            list[3] = newChild.getPassword();
            model.addRow(list);
            jTextField1.setText("");
            jTextField2.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Hata Mesajı", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

